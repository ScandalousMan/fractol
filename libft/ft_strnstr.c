#include "libft.h"

char	*ft_strnstr(const char *m, const char *a, size_t len)
{
	size_t	i[3];

	i[0] = 0;
	i[1] = 0;
	i[2] = 0;
	while (m[i[0]] != '\0' && i[2] == 0 && i[0] < len)
	{
		if (m[i[0]] == a[0])
		{
			while (i[0] + i[1] < len && m[i[0] + i[1]] != '\0' &&
					a[i[1]] != '\0' && a[i[1]] == m[i[0] + i[1]])
				i[1]++;
			if (a[i[1]] == '\0')
				i[2] = 1;
			else
				i[1] = 0;
		}
		i[0]++;
	}
	if (a[0] == '\0')
		return ((char*)m);
	if (i[2] != 0)
		return ((char*)(m + i[0] - 1));
	else
		return (NULL);
}
