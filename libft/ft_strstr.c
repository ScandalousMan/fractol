#include "libft.h"

char	*ft_strstr(const char *m, const char *a)
{
	int	i[3];

	i[0] = 0;
	i[1] = 0;
	i[2] = 0;
	while (m[i[0]] != '\0' && i[2] == 0)
	{
		if (a[0] == m[i[0]])
		{
			while ((m[i[1] + i[0]]) && a[i[1]] == m[i[0] + i[1]] && !(i[2]))
				i[1]++;
			if (a[i[1]] == '\0')
				i[2] = 1;
			else
				i[1] = 0;
		}
		i[0]++;
	}
	if (a[0] == '\0')
		return ((char*)m);
	else if (i[2] != 0)
		return ((char*)(m + i[0] - 1));
	else
		return (NULL);
}
